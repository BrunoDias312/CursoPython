"""
Descobrir qual numero é maior que o outro
"""
#Entrada de valores
numA = float(input("Digite o valor: "))
numB = float(input("Digite o segundo valor: "))

#Encontrar o valor maior - Saida de dados
if numA > numB:
    print(f"O primeiro valor digitado é maior que o segundo!!!"
          f" O numero digitado é {numA}")
else:
    print(f"O segundo valor digitado é maior que o primeiro!!!"
          f" O numero digitado é {numB}")