"""
divider entre 3 pessoas o valor de R$780.000,00
1 - 46%
2 - 32%
3 - 18%
"""

premio = 780000

ganhador1 = premio * 0.46
ganhador2 = premio * 0.32
ganhador3 = premio * 0.22

print(f"Ganhador 1 com 46% do premio fica com R${ganhador1:,.2f}.")
print(f"Ganhador 2 com 32% do premio fica com R${ganhador2:,.2f}.")
print(f"Ganhador 3 com 18% do premio fica com R${ganhador3:,.2f}.")
print("-->>> Do premio total de R$780.000,00. <<<--")