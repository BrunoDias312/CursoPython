"""
Leia tres valores e apresente eles somando ao quadrado
"""

valor_1 = float(input("Valor 1: "))

valor_2 = float(input("Valor 1: "))

valor_3 = float(input("Valor 1: "))

valor_1 = valor_1 ** 2

valor_2 = valor_2 ** 2

valor_3 = valor_3 ** 2

valor_total = valor_1 + valor_2 + valor_3

print(valor_1)
print(valor_2)
print(valor_3)
print("valor total: ", valor_total)
